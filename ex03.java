package domain;

public class HelloWorld {

    public static void main(String[] args) {
        
        /*
        == Operador de Igual
        < Operador de Menor
        > Operador de Maior
        <= Operador de Menor ou igual
        >= Operador de Maior ou igual
        != Operador de diferente
        
        */
        
        int valor1 = 20;
        int valor2 = 10;
        
        System.out.println(valor1 == valor2);
        System.out.println(valor1 < valor2);
        System.out.println(valor1 > valor2);
        System.out.println(valor1 <= valor2);
        System.out.println(valor1 >= valor2);
        System.out.println(valor1 != valor2);
    }

}
